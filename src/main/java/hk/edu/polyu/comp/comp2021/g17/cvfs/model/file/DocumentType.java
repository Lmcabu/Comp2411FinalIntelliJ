package hk.edu.polyu.comp.comp2021.g17.cvfs.model.file;

/**
 * An enum class represents Document Type.
 * A document must be one of these types: .java, .html, .css, .txt
 *
 * @see Document
 */
public enum DocumentType {
    /**
     * java Document Type
     */
    java,
    /**
     * html Document Type
     */
    html,
    /**
     * css Document Type
     */
    css,
    /**
     * txt Document Type
     */
    txt
}
