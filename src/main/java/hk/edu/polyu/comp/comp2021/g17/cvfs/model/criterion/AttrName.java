package hk.edu.polyu.comp.comp2021.g17.cvfs.model.criterion;


/**
 * This enum class represents all possible attribute name of a Criterion
 *
 * @see Criterion
 */
public enum AttrName {
    /**
     * For size criterion
     */
    size,
    /**
     * For type criterion
     */
    type,
    /**
     * For name criterion
     */
    name,
    /**
     * For criterion isDocument
     */
    filetype,
    /**
     * For composite criterion
     */
    composite
}


